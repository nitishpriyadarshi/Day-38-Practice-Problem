let number = 5;

switch(number){
    case 1:
        console.log("One");
        break;
    case 2:
        console.log("Ten");
        break;   
    case 3:
        console.log("Hundred");
        break;
    case 4:
        console.log("Thousand");
        break;
    default:
        console.log("Invalid Input");
}